    private void selectImage_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectImage_buttonActionPerformed
        fileChooser.showOpenDialog(null);
        selected = fileChooser.getSelectedFile();
        if(selected.isFile()){
            image_path.setText(selected.getAbsolutePath());
        }
        
    }//GEN-LAST:event_selectImage_buttonActionPerformed

    private void drawSelectedImage_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_drawSelectedImage_buttonActionPerformed

        Graphics2D graphics = (Graphics2D) image_panel.getGraphics();
        
        int width = 200;
        int height = 200;
        
        try {
            
            image = ImageIO.read(selected);
            
            if(image == null){
                JOptionPane.showMessageDialog(null, "Selected Image Unavailable");
            }
            
            graphics.drawImage(image, 0, 0, image_panel.getWidth(), image_panel.getHeight(), null);
            
            image.flush();
            
        } catch (IOException ex) {
            Logger.getLogger(image_viewer_form.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_drawSelectedImage_buttonActionPerformed

    private void saveImage_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveImage_buttonActionPerformed
              
        if(image == null){
            JOptionPane.showMessageDialog(null, "Select image and draw selected image ");
        }else{
        
        fileSaver.showSaveDialog(null);
        String save_path = fileSaver.getSelectedFile().getPath();
        
        File output_image = new File(save_path+"/saveimage.jpeg");
        
        try {
            ImageIO.write(image, "png", output_image);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Image Can't Save");
            Logger.getLogger(image_viewer_form.class.getName()).log(Level.SEVERE, null, e);
        }
        
        fileSaver.removeAll();
        
        }
               
    }//GEN-LAST:event_saveImage_buttonActionPerformed

        
    
