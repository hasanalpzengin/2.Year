    private void update_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_buttonActionPerformed
        
        login_table.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {

            },
            new String [] {
                "No", "Kullanıcı Adı", "Şifre", "Hesap Tipi"
            }
        ) {
            boolean[] canEdit = new boolean [] {
                false, false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
        
        DefaultTableModel model = (DefaultTableModel) login_table.getModel();
        
        try {
            Connection connect = new db_connection().connect;
            Statement stm = (Statement) connect.createStatement();
            ResultSet rs = (ResultSet) stm.executeQuery("SELECT kullanici_adi,sifre,akademisyen_id FROM admin_giris");
            
            while(rs.next()){
                model.addRow(new Object[]{
                    rs.getInt("akademisyen_id"),
                    rs.getString("kullanici_adi"),
                    rs.getString("sifre"),
                    "Akademisyen"
                });
            }
            stm = (Statement) connect.createStatement();
            rs = (ResultSet) stm.executeQuery("SELECT kullanici_adi,sifre,ogrenci_id FROM ogrenci_giris");
            
            while(rs.next()){
                model.addRow(new Object[]{
                    rs.getInt("ogrenci_id"),
                    rs.getString("kullanici_adi"),
                    rs.getString("sifre"),
                    "Ogrenci"
                });
            }
            
            
            
            login_table.setModel(model);
            
            stm.close();
            connect.close();
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(login_gui.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_update_buttonActionPerformed

