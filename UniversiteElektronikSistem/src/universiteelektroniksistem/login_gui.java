    private void add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_buttonActionPerformed
        try {
            Connection connect = new db_connection().connect;
            PreparedStatement prep_stm;
            
            int getSelected = info4_cb.getSelectedIndex();
            
            //if login information belong to academician
            if(getSelected == 0){
                prep_stm = (PreparedStatement) connect.prepareStatement("insert into admin_giris(akademisyen_id,kullanici_adi,sifre) values(?,?,?)");

                prep_stm.setInt(1, Integer.parseInt(info1_tf.getText()));
                prep_stm.setString(2, info2_tf.getText());
                prep_stm.setString(3, info3_tf.getText());

                prep_stm.execute();
            }else{//else login information belong to student
                prep_stm = (PreparedStatement) connect.prepareStatement("insert into ogrenci_giris(ogrenci_id,kullanici_adi,sifre) values(?,?,?)");

                prep_stm.setInt(1, Integer.parseInt(info1_tf.getText()));
                prep_stm.setString(2, info2_tf.getText());
                prep_stm.setString(3, info3_tf.getText());

                prep_stm.execute();
            }
            
            //insert log
            
            Statement delete_log = (Statement) connect.createStatement();
                delete_log.executeUpdate("DELETE FROM log ORDER BY date ASC limit 1");
                delete_log.close();
            
            PreparedStatement prep_stm2 = (PreparedStatement) connect.prepareStatement("INSERT INTO log(date,userid,log) VALUES(?,?,?) ");
            prep_stm2.setString(1, new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(Calendar.getInstance().getTime()));
            prep_stm2.setString(2, UserInfo.userID);
            prep_stm2.setString(3, info2_tf.getText()+" Login Inserted");
            prep_stm2.execute();
            
            JOptionPane.showMessageDialog(rootPane, "Giriş Bilgisi Eklendi.");
            addLoginFrame.setVisible(false);
            
            prep_stm2.close();
            prep_stm.close();
            connect.close();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, "Girdiğiniz Bilgilerde Yanlışlık Var !!");
            Logger.getLogger(login_gui.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_add_buttonActionPerformed

