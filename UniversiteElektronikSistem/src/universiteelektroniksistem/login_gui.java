    private void delete_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_buttonActionPerformed
        int reply = JOptionPane.showConfirmDialog(null, "Kayidin silinmesine emin misiniz ?", "Silinsin mi?",  JOptionPane.YES_NO_OPTION);
        
        if(reply == JOptionPane.YES_OPTION){
            try {
                Connection connect = new db_connection().connect;
                //delete student or academician's login info
                PreparedStatement prep_stm = (PreparedStatement) connect.prepareStatement("DELETE FROM ogrenci_giris WHERE kullanici_adi = ? ");
                prep_stm.setString(1, delete_tf.getText());
                prep_stm.execute();
                prep_stm.close();
                prep_stm = (PreparedStatement) connect.prepareStatement("DELETE FROM admin_giris WHERE kullanici_adi = ? ");
                prep_stm.setString(1, delete_tf.getText());
                prep_stm.execute();
                
                //insert log
                
                Statement delete_log = (Statement) connect.createStatement();
                delete_log.executeUpdate("DELETE FROM log ORDER BY date ASC limit 1");
                delete_log.close();
                
                PreparedStatement prep_stm2 = (PreparedStatement) connect.prepareStatement("INSERT INTO log(date,userid,log) VALUES(?,?,?) ");
                prep_stm2.setString(1, new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(Calendar.getInstance().getTime()));
                prep_stm2.setString(2, UserInfo.userID);
                prep_stm2.setString(3, delete_tf.getText()+" Login Deleted");
                prep_stm2.execute();
                
                prep_stm2.close();
                prep_stm.close();
                connect.close();
                JOptionPane.showMessageDialog(rootPane, "Giriş Bilgisi Silindi");
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(login_gui.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_delete_buttonActionPerformed

