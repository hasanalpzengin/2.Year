    public void search(String search_element){
        int count = 0;
        department_table.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {

            },
            new String [] {
                "Bölüm No", "Bölüm İsmi", "Bölüm Başkanı", "Bölüm Telefon No", "Bölüm Öğrenci Sayısı"
            }
        ) {
            boolean[] canEdit = new boolean [] {
                false, false, false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
        
        DefaultTableModel model = (DefaultTableModel) department_table.getModel();
        
        try {
            Connection connect = new db_connection().connect;
            Statement stm = (Statement) connect.createStatement();
            ResultSet rs = (ResultSet) stm.executeQuery("SELECT bolum_id,bolum_ismi,bolum_baskani,bolum_telno,bolum_ogrencisayisi FROM bolumler");
            
            while(rs.next()){
                if(String.valueOf(rs.getInt("bolum_id")).contains(search_element) || rs.getString("bolum_ismi").contains(search_element) || rs.getString("bolum_baskani").contains(search_element) || rs.getString("bolum_telno").contains(search_element) || String.valueOf(rs.getInt("bolum_ogrencisayisi")).contains(search_element)){
                    model.addRow(new Object[]{
                        rs.getInt("bolum_id"),
                        rs.getString("bolum_ismi"),
                        rs.getString("bolum_baskani"),
                        rs.getString("bolum_telno"),
                        rs.getInt("bolum_ogrencisayisi"),
                    });
                    
                    count++;
                }
            }
            
            department_table.setModel(model);
            
            JOptionPane.showMessageDialog(rootPane, "Aramanız için "+count+" kadar veri bulundu.");
            
            connect.close();
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(department_gui_nA.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
