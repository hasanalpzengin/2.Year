    private void update_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_buttonActionPerformed
        UserInfo uinfo = new UserInfo();
        int toplamKredi = 0;
        
        courses_table.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {

            },
            new String [] {
                "Ders ID", "Ders Ismi", "Ders Kredisi"
            }
        ) {
            boolean[] canEdit = new boolean [] {
                false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
        
        DefaultTableModel model = (DefaultTableModel) courses_table.getModel();
        
        try {
            Connection connect = new db_connection().connect;
            PreparedStatement prep_stm = (PreparedStatement) connect.prepareStatement("SELECT ders_id,ders_kredisi FROM secilen_dersler WHERE ogrenci_id=?");
            prep_stm.setInt(1, Integer.parseInt(uinfo.userID));
            ResultSet rs = (ResultSet) prep_stm.executeQuery();
            
            while(rs.next()){
                
                PreparedStatement prep_stm2 = (PreparedStatement) connect.prepareStatement("SELECT ders_ismi FROM dersler WHERE ders_id=?");
                prep_stm2.setInt(1, rs.getInt("ders_id"));
                ResultSet rs2 = (ResultSet) prep_stm2.executeQuery();
                
                rs2.next();
                
                toplamKredi += rs.getInt("ders_kredisi");
                
                model.addRow(new Object[]{
                    rs.getInt("ders_id"),
                    rs2.getString("ders_ismi"),
                    rs.getInt("ders_kredisi"),
                });
            }
            
            courses_table.setModel(model);
            
            credit_label.setText(String.valueOf(toplamKredi));
            
            connect.close();
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(courseRegister_gui.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_update_buttonActionPerformed

