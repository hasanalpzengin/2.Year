    private void add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_buttonActionPerformed
        UserInfo uinfo = new UserInfo();

        try {
            Connection connect = new db_connection().connect;
            
            PreparedStatement prep_stm = (PreparedStatement) connect.prepareStatement("SELECT ders_kredisi FROM dersler WHERE ders_id=?");
            prep_stm.setInt(1, courses_cb.getSelectedIndex());
            ResultSet rs = (ResultSet) prep_stm.executeQuery();
            rs.next();
            
            PreparedStatement prep_stm2 = (PreparedStatement) connect.prepareStatement("insert into secilen_dersler(ogrenci_id,ders_id,ders_kredisi) values(?,?,?)");
            
            prep_stm2.setInt(1, Integer.parseInt(uinfo.userID));
            prep_stm2.setInt(2, courses_cb.getSelectedIndex());
            prep_stm2.setInt(3, rs.getInt("ders_kredisi"));
            
            prep_stm2.execute();
            
            JOptionPane.showMessageDialog(rootPane, "Ders Eklendi.");
            addCourseFrame.setVisible(false);
            
            connect.close();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, "Girdiğiniz Bilgilerde Yanlışlık Var !!.");
            Logger.getLogger(courseRegister_gui.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_add_buttonActionPerformed

    private void addCourseFrameWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_addCourseFrameWindowActivated
        
        UserInfo uinfo = new UserInfo();
        courses_cb.removeAllItems();
        
        try {
            Connection connect = new db_connection().connect;
            PreparedStatement prep_stm = (PreparedStatement) connect.prepareStatement("SELECT bolum_id,ogrenci_sinifi FROM ogrenciler WHERE ogrenci_id=?");
            prep_stm.setInt(1, Integer.parseInt(uinfo.userID));
            ResultSet rs = (ResultSet) prep_stm.executeQuery();
            rs.next();
            PreparedStatement prep_stm2 = (PreparedStatement) connect.prepareStatement("SELECT ders_ismi FROM dersler WHERE bolum_id=? AND ders_sinifi=?");
            prep_stm2.setInt(1, rs.getInt("bolum_id"));
            prep_stm2.setInt(2, rs.getInt("ogrenci_sinifi"));
            ResultSet rs2 = (ResultSet) prep_stm2.executeQuery();
            while(rs2.next()){
                courses_cb.addItem(rs2.getString("ders_ismi"));
            }
            
            prep_stm.close();
            prep_stm2.close();
            connect.close();
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(courseRegister_gui.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addCourseFrameWindowActivated

