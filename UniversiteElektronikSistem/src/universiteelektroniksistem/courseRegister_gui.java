    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        UserInfo uinfo = new UserInfo();
        
        int reply = JOptionPane.showConfirmDialog(null, "Kayidin silinmesine emin misiniz ?", "Silinsin mi?",  JOptionPane.YES_NO_OPTION);
        
        if(reply == JOptionPane.YES_OPTION){    
            try {
                Connection connect = new db_connection().connect;
                PreparedStatement prep_stm = (PreparedStatement) connect.prepareStatement("DELETE FROM secilen_dersler WHERE ders_id = ? AND ogrenci_id=? ");
                prep_stm.setInt(1, Integer.parseInt(delete_tf.getText()));
                prep_stm.setInt(2, Integer.parseInt(uinfo.userID));
                prep_stm.execute();
                prep_stm.close();
                connect.close();
                JOptionPane.showMessageDialog(rootPane, "Ders Silindi");
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(courses_gui.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    
